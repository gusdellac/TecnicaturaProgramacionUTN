# profundizando en sistemas numericos

# sistema decimal
a = 10
print(f"a decimal: {a}")
# sistema binario
a = 0b1010
print(f"a binario: {a}")

# sistema octal
a = 0o12
print(f"a octal: {a}")

# sistema hexadecimal
a = 0xA
print(f"a hexadecimal : {a}")

# int("10", 10) - constructor int - 1er parametro valor a trabajar 2do parametro base numerica

# Base decimal
a = int("10", 10)
print(f"a = base decimal: {a}")

# Base binaria
a = int("1010", 2)
print(f"a = base binaria: {a}")

# Base Octal
a = int("12", 8)
print(f"a = base octal: {a}")

# Base hexadecimal
a = int("A", 16)
print(f"a = base hexadecimal: {a}")

# Base 5 sus valores son de 0 a 4
a = int("20", 5)
print(f"a = Base 5: {a}")

